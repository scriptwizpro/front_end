function add(a, b) {
  return a + b;
}
function subtract(a, b) {
  return a - b;
}
function multiply(a, b) {
  return a * b;
}
function divide(a, b) {
  return a / b;
}

console.log("Add:", add(2, 3));
console.log("Subtract:", subtract(5, 2));
console.log("Multiply:", multiply(4, 5));
console.log("Divide:", divide(10, 2))